<template>
  <div
    :class="wrapCls"
  >
    <slot></slot>
  </div>
</template>
<script>
/**
 * afo-tag
 * @module components/tag
 * @desc 标签
 * @param {boolean} [plain=false] - 是否是空心
 * @param {boolean} [mark=false] - 是否是圆角
 * @param {string} [type] - 接受default, primary, danger
 *
 * @example
 * <afo-tag mark type="danger">标签</afo-tag>
 */
export default {
  name: 'afo-tag',
  props: {
    type: {
      type: String,
      default: 'default',
      validator (value) {
        return [
          'default', 'danger', 'primary'
        ].indexOf(value) > -1
      }
    },
    plain: {
      type: Boolean,
      default: false
    },
    mark: {
      type: Boolean,
      default: false
    }
  },
  computed: {
    wrapCls () {
      return [
        `afo-tag--${this.plain ? this.type + 'plain' : this.type}`,
        {'afo-tag--mark': this.mark},
        {'afo-tag': true}
      ]
    }
  }
}
</script>
